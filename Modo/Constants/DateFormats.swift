import Foundation

/// Date format constants used throughout the application
enum DateFormats {
    /// Standard date format: yyyy-MM-dd (e.g., "2025-01-27")
    /// Used for Firebase keys, cache keys, and date serialization
    static let standardDate: String = "yyyy-MM-dd"
    
    /// Time format: HH:mm (e.g., "14:30")
    static let time: String = "HH:mm"
}

